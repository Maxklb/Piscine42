#include <unistd.h>

void	ft_putchar(char *str)
{
	int	index;

	index = 0;
	while (str[index] != '\0')
	{
		write(1, &str[index], 1);
		index++;
	}
}

int	main(int argc, char **argv)
{
	ft_putchar(argv[0]);
	if (argc != 2)
		return (0);
}
