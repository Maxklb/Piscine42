#include "ft.h"

int	ft_atoi(char *str)
{
	int	res;
	int	m;

	res = 0;
	m = 1;
	while ((*str && *str >= 9 && *str <= 13) || *str == 32)
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			m = -m;
		str++;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	return (m * res);
}
