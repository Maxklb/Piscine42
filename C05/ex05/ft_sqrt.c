int	ft_sqrt(int nb)
{
	unsigned int	n;

	n = 1;
	if (nb < 1)
		return (0);
	while (n * n < (unsigned int)nb)
		n++;
	if (n * n > (unsigned int)nb)
		return (0);
	return (n);
}
